#include  <ctype.h>

#include  "fWild.h"

/* ----------------------------------------------------------------------- *\
|  Skip over all leading white space
\* ----------------------------------------------------------------------- */
	char *
stpblk (
	char *s)

	{
	while (isspace(*s))
		++s;

	return (s);
	}

